import java.util.Scanner;

public class switchCase {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        switch(num){
            case 1:
                System.out.println("monday");
                break;
            case 2:
                System.out.println("monday");
                break;
            case 3:
                System.out.println("monday");
                break;
            case 4:
                System.out.println("monday");
                break;
            case 5:
                System.out.println("monday");
                break;
            case 6:
                System.out.println("monday");
                break;
            case 7:
                System.out.println("monday");
                break;
            default:
                System.out.println("Please enter the number between 1 to 7");


        }
        sc.close();
    }
    
}
